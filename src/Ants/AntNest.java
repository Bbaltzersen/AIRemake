public class AntNest {
    private int carriers;
    private int warriors;
    private int scouts;

    public int getCarriers() {
        return carriers;
    }

    public void setCarriers(int carriers) {
        this.carriers = carriers;
    }

    public int getWarriors() {
        return warriors;
    }

    public void setWarriors(int warriors) {
        this.warriors = warriors;
    }

    public int getScouts() {
        return scouts;
    }

    public void setScouts(int scouts) {
        this.scouts = scouts;
    }
    
    
}
